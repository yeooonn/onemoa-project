 - 변경 전 ''3''항의 하단 본문 내용은 변경 후 ''나''항에 포함되는 내용으로, 삭제하였습니다.
 - ''라''항을 신설하였습니다.
 제5조 (개인정보의 제3자 제공)  - 변경 전 ''1''항의 일부 표를 삭제하였습니다.
 제6조 (개인정보의 처리 위탁)   - 변경 전 ''1''항의 일부 표를 변경하였습니다.
 제7조 (개인정보 파기절차 및 방법) 
 - 변경 전 ''1''항 1)과 ''2''항 1)의 일부 내용과 표를 변경하였습니다. 
 - ''다''항을 신설하였습니다.
 제8조 (회원 및 법정대리인의 권리·의무 행사방법)  - 변경 전 ''1''항 ''2''항의 내용을 변경 후 ''가,나,다''항으로  분리·이동하고,‘라’항의 순서를 조정하였습니다.
 제12조 (고지의 의무)   - 변경 전 내용을 변경 후 ''가,나,다,라''항으로 분리·이동하고, 일부 내용을 변경하였습니다. 
 개인정보처리방침 전문  
 - 개인정보처리방침 전문에 사용된 일부 용어를 변경하였습니다.
  | 이용자 ≫ 회원
  | 고객 ≫ 회원
  | 판매자 ≫ 전문가
 

